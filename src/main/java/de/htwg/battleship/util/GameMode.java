package de.htwg.battleship.util;

/**
 * GameMode is an enum for the different modes of the Game.
 *
 * @author Moritz Sauter <SauterMoritz@gmx.de>
 * @version 1.00
 * @since 2015-01-23
 */
public enum GameMode {
    /**
     * Mode to indicate the usual GameMode.
     */
    NORMAL,
    /**
     * Mode to indicate that wich player has to shot always changes after one
     * round.
     */
    EXTREME
}
